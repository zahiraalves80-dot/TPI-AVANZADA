    pantallaInicio.setVisible(true);
    
    // 3. (Opcional pero recomendado) Centra la nueva ventana
    pantallaInicio.setLocationRelativeTo(null);
    
    // 4. Cierra la ventana actual de Registro
    this.dispose();
        
        // Limpiar campos
        jTextFieldNombreUsuario.setText("");
