private void mostrarQR(String texto, String nombreGato) {
    try {
        // 1. Generar la matriz de bits del QR
        QRCodeWriter qrWriter = new QRCodeWriter();
        BitMatrix bitMatrix = qrWriter.encode(texto, BarcodeFormat.QR_CODE, 300, 300); // Tama帽o 300x300
        
        // 2. Convertir la matriz a una imagen
        BufferedImage qrImage = MatrixToImageWriter.toBufferedImage(bitMatrix);
        
        // 3. Crear el PopUp (JDialog)
        JDialog qrDialog = new JDialog(this, "QR para: " + nombreGato, true); // true = modal
        qrDialog.setLayout(new java.awt.FlowLayout());
        
        // 4. Crear el contenedor de la imagen
        JLabel qrLabel = new JLabel(new ImageIcon(qrImage));
        
        // 5. Mostrar
        qrDialog.add(qrLabel);
        qrDialog.pack(); // Ajusta el tama帽o de la ventana a la imagen
        qrDialog.setLocationRelativeTo(this); // Centra el popup
        qrDialog.setVisible(true);
        
    } catch (Exception e) {
        // Este catch es para la biblioteca Zxing
        JOptionPane.showMessageDialog(this, "Error al crear la imagen QR.", "Error Zxing", JOptionPane.ERROR_MESSAGE);
    } 
    }//GEN-LAST:event_jButton3ActionPerformed

    private void cargarGatosDisponibles() {
    try {
        List<Gato> gatos = control.traerGatosDisponibles(); 
        
        DefaultTableModel modeloTabla = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        
        // 1. Definir solo las Columnas que quieres mostrar
        String titulos[] = {"ID", "Nombre", "Raza"};
        modeloTabla.setColumnIdentifiers(titulos);
        
        // 2. Llenar la tabla
        if (gatos != null && !gatos.isEmpty()) {
            for (Gato g : gatos) {
                Object[] objeto = {
                    g.getIdGato(), // Lo necesitamos para la l贸gica
                    g.getNombre(),
                    g.getRaza(),
                };
                modeloTabla.addRow(objeto);
            }
        }
        
        jTable1.setModel(modeloTabla);
        
        // 3. (Opcional) Ocultar la columna ID (Columna 0)
        jTable1.getColumnModel().getColumn(0).setMaxWidth(0);
        jTable1.getColumnModel().getColumn(0).setMinWidth(0);
        jTable1.getColumnModel().getColumn(0).setPreferredWidth(0);
        
    } catch (OperacionException e) {
        JOptionPane.showMessageDialog(this, e.getMessage(), "Informaci贸n", JOptionPane.INFORMATION_MESSAGE);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error inesperado al cargar gatos: " + e.getMessage(), "Error Cr铆tico", JOptionPane.ERROR_MESSAGE);
        e.printStackTrace();
    }
}
